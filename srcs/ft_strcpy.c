#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int cnt;

	cnt = 0;
	while (src[cnt])
	{
		dst[cnt] = src[cnt];
		cnt++;
	}
	dst[cnt] = '\0';
	return (dst);
}
