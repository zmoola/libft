#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*buf;
	size_t	cnt;

	cnt = 0;
	buf = (char *)s;
	while (cnt < n)
	{
		buf[cnt] = 0;
		cnt++;
	}
}
