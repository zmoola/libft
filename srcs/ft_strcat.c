#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int cn;

	cn = 0;
	while (s1[cn])
		cn++;
	while (*s2)
	{
		s1[cn] = *s2;
		cn++;
		s2++;
	}
	s1[cn] = '\0';
	return (s1);
}
